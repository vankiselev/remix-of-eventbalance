-- Возвращаем возможность всем сотрудникам видеть отпуска друг друга
-- Удаляем ограничительную политику
DROP POLICY IF EXISTS "Пользователи видят только свои отпуска" ON public.vacations;

-- Создаем политику, позволяющую всем аутентифицированным пользователям видеть все отпуска
CREATE POLICY "Все сотрудники могут видеть отпуска коллег" 
ON public.vacations 
FOR SELECT 
USING (true);