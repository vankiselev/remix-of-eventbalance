/**
 * Escapes HTML special characters to prevent XSS when interpolating
 * user-provided strings into HTML templates (e.g. print windows).
 */
export function escapeHtml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}
